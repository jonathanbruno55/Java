package uppercase;

public class UpperCase {

    public static void main(String[] args) {
        
        String str = "abcdef ghijkl mnopqr stuvwxyz";
        
        String up = str.toUpperCase();
        
        System.out.println("Original string: " + str);
        System.out.println("Upper case: " + up);
    }
    
}
